	DWColor::DWColor():
		rgb_byte_(0xFFFFFF)
	{
		rgba_float_.f[0] = 1.0;
		rgba_float_.f[1] = 1.0;
		rgba_float_.f[2] = 1.0;
		rgba_float_.f[3] = 1.0;
	}
	DWColor::DWColor(float r, float g, float b, float a) :
		rgba_float_({ r, g, b, a })
	{
		uint32_t r32 = static_cast<uint32_t>(round((rgba_float_.f[0] / 1.0 ) * 255.0));
		uint32_t g32 = static_cast<uint32_t>(std::round((rgba_float_.f[0] / 1.0) * 255.0));
		uint32_t b32 = static_cast<uint32_t>(std::round((rgba_float_.f[0] / 1.0) * 255.0));
		rgb_byte_ = (b32 << 16) | (b32 << 8) | b32;
	}

	DWColor::DWColor(uint8_t r, uint8_t g, uint8_t b, uint8_t a)
	{
		uint32_t r32 = r;
		uint32_t g32 = g;
		uint32_t b32 = b;
		rgb_byte_ = (r32 << 16) | (g32 << 8) | b32;
		rgba_float_.f[0] = static_cast<float>(r) / 255.0;
		rgba_float_.f[1] = static_cast<float>(g) / 255.0;
		rgba_float_.f[2] = static_cast<float>(b) / 255.0;
		rgba_float_.f[3] = static_cast<float>(a) / 255.0;
	}

	DWColor::DWColor(uint32_t color):
		rgb_byte_(color)
	{
		rgba_float_.f[0] = static_cast<float>((rgb_byte_ >> 16) & 0xFF) / 255.0;
		rgba_float_.f[1] = static_cast<float>((rgb_byte_ >> 8) & 0xFF) / 255.0;
		rgba_float_.f[2] = static_cast<float>(rgb_byte_ & 0xFF) / 255.0;
		rgba_float_.f[3] = 1.0;
	}

