	void DWColor::SetByteR(uint8_t r)
	{
		rgba_float_.f[0] = static_cast<float>(r) / 255.0;
		uint32_t r32 = r;
		rgb_byte_ = r32 << 16 | (rgb_byte_ & 0x0000FFFF);
	}

