	{
		return rgba_float_.f[0];
	}

	void DWColor::SetFloatR(float r)
	{
		rgba_float_.f[0] = r;
		uint32_t r32 = static_cast<uint32_t>(std::round((r / 1.0) * 255.0));
		rgb_byte_ = (r32 << 16) | (rgb_byte_ & 0x0000FFFF);
	}

	float DWColor::GetFloatG()
	{
		return rgba_float_.f[1];
	}

	void DWColor::SetFloatg(float g)
	{
		rgba_float_.f[1] = g;
		uint32_t g32 = static_cast<uint32_t>(std::round((g / 1.0) * 255.0));
		rgb_byte_ = (g32 << 8) | (rgb_byte_ & 0x00FF00FF);
	}

